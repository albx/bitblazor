namespace BitBlazor.Utilities;

/// <summary>
/// Defines icon alignment options.
/// </summary>
public enum IconAlignment
{
    /// <summary>
    /// Default alignment.
    /// </summary>
    Default,
    /// <summary>
    /// Aligns icon to the bottom.
    /// </summary>
    Bottom,
    /// <summary>
    /// Aligns icon to the middle.
    /// </summary>
    Middle,
    /// <summary>
    /// Aligns icon to the top.
    /// </summary>
    Top
}
