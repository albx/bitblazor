namespace BitBlazor.Components;

/// <summary>
/// Specifies the shadow size for a card element.
/// </summary>
/// <remarks>
/// This enumeration is typically used to define the visual appearance of a card's shadow in a user interface. 
/// The values represent predefined shadow sizes that can be applied to a card.
/// </remarks>
public enum CardShadow
{
    /// <summary>
    /// A small shadow
    /// </summary>
    Small,

    /// <summary>
    /// A medium shadow
    /// </summary>
    Medium,

    /// <summary>
    /// A large shadow
    /// </summary>
    Large
}
