namespace BitBlazor;

/// <summary>
/// Represents common aspect ratios used in media and display settings.
/// </summary>
/// <remarks>
/// The <see cref="Ratio"/> enumeration provides predefined aspect ratios, such as 1:1, 4:3, 16:9, and 21:9.
/// </remarks>
public enum Ratio
{
    /// <summary>
    /// Represents a 1:1 aspect ratio
    /// </summary>
    Ratio1x1,

    /// <summary>
    /// Represents a 4:3 aspect ratio
    /// </summary>
    Ratio4x3,

    /// <summary>
    /// Represents a 16:9 aspect ratio
    /// </summary>
    Ratio16x9,

    /// <summary>
    /// Represents the aspect ratio of 21:9
    /// </summary>
    Ratio21x9,
}
